import { useState } from "react";

const useCounter = (initial, stock) => {
  const [count, setCount] = useState(initial);

  const increment = () => {
    if (count < stock) {
      setCount(count + 1);
    }
  };

  const decrement = () => {
    if (count > 0) {
      setCount(count - 1);
    }
  };

  return { count, increment, decrement };
};

export default useCounter;